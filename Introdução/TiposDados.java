package Introdução;

public class TiposDados {
    public static void main(String[] args){
        int inteiro = 10;
        double pontoFlutuante = 25.4;
        String caractere = "Sou uma cadeia de caracteres";
        System.out.println("Inteiro: " + inteiro + "Ponto Flutuante: " + pontoFlutuante + "Caractere: " + caractere);

        //Outros tipos
        float numero0 = 3.1f;
        long numero1 = 4567;
        short numero2 = 122;
        byte numero3 = 46;
        System.out.println("float: " + numero0 + "Long: " + numero1 + "short: " + numero2 + "byte: " + numero3);
    }
}
